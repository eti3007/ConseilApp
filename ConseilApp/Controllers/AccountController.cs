        [AllowAnonymous]
        public ActionResult Register()
        {
            ViewBag.Page = "Register";
            AccountViewModel model = new AccountViewModel();

            // Mise en Session de la liste des styles
            if (!IsSetSession(SessionKey.ListeStyle)) SetSession(SessionKey.ListeStyle, this._StyleService.RecupereListeDesStyles());

            model.styleListe = DropDownListBuilder<DropDownListeStyle>.CreateDropDownList(this._StyleService.RecupereListeDesStylesPourDDL());
            model.genreListe = new List<SelectListItem>() { new SelectListItem() { Selected = true, Text = "Femme", Value = "F" }, new SelectListItem() { Text = "Homme", Value = "H" } };

            return View(model);
        }

        //
        // POST: /Account/Register

        [HttpPost]
        [AllowAnonymous]
        [ValidateAntiForgeryToken]
        public ActionResult Register(AccountViewModel model)
        {
            if (ModelState.IsValid)
            {
                // Attempt to register the user
                try
                {
                    WebSecurity.CreateUserAndAccount(model.UserName, model.Password);

                    // Appel la méthode pour ajouter l'enrgistrement dans la table Personne en récupérant l'ID dans la table UserProfil
                    int personneId = this._PersonneService.AjouteNouveau(this._PersonneBuilder.PersonneToRegister(model), Convert.ToInt32(model.Style), model.UserName);

                    if (personneId == 0) RedirectToRoute("Default");

                    base.RegisterPerson(personneId, model.UserName, model.Style);
                    base.SetSession(SessionKey.ListeStyle, this._StyleService.RecupereListeDesStyles());

                    base.SetSession(SessionKey.PersonneStatut, 1);

                    WebSecurity.Login(model.UserName, model.Password);

                    return RedirectToAction("UploadPhotos", "Upload", new { vetementStyleId = base.StyleEnCours, vetementId = 0, habillageStyleId = 0 });
                }
                catch (MembershipCreateUserException e)
                {
                    ModelState.AddModelError("", ErrorCodeToString(e.StatusCode));
                }
            }

            // If we got this far, something failed, redisplay form
            model.styleListe = DropDownListBuilder<DropDownListeStyle>.CreateDropDownList(this._StyleService.RecupereListeDesStylesPourDDL());
            model.genreListe = new List<SelectListItem>() { new SelectListItem() { Selected = true, Text = "Femme", Value = "F" }, new SelectListItem() { Text = "Homme", Value = "H" } };
            return View(model);
        }
